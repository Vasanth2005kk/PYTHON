'''
range functions
'''
print(list(range(5)))
print(list(range(1,10)))
print(list(range(0,20,2)))# even numbers
print(list(range(1,21,2)))# odd numbers
print(list(range(5,0,-1)))# reverse process
print(list(range(5,0,-2)))# reverse proces in odd number
print(list(range(20,0,-2)))# reverse proces in even number