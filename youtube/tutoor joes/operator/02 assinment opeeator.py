'''                          syntex
= assinment                  a=b

+= addition                  a+=b ---> a=a+b ( all )
-= subtraction
*= multiplication
/= division
%= modulus
**= exponentiation
//= floor division
&= bitwise AND operator
|= bitwise OR operator
^= bitwise XOR operator
>>= bitwise right shift assignment operator
<<= bitwise lift shift assignment operator
'''
a=100
b=12
print("\n")
a+=b   #a=a+12   --> 112=100+12
print(a)
a-=b   #a=112-12 --> 100 this for all continue
print(a)
a*=b
print(a)
a/=b
print(a)
a%=b
print(a)
c=20
